# Assignment Operators
# +=
prize_money = 500
prize_money = prize_money + 50
print(prize_money)
prize_money += 75
print(prize_money)
# -=
prize_money -= 55
print(prize_money)
# *=
prize_money *= 10
print(prize_money)
# /= , it will change to float
prize_money /= 10
print(prize_money)
print(type(prize_money))
# carefully
print(prize_money - 1)
# this will not change
print(prize_money)
